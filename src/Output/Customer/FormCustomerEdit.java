    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String namaPelanggan = inputNamaPelanggan.getText().trim();
        String telpon = inputNomorTelpon.getText().trim();
        String alamat = inputAlamat.getText().trim();
        if (namaPelanggan.isEmpty() || telpon.isEmpty() || alamat.isEmpty()) {
            this.peringatan("Data tidak boleh kosong!");
        } else {
            try {
                String query = "UPDATE customer SET customer_fullname=?, customer_alamat=?, customer_telepon=? WHERE customer_id=?;";
                PreparedStatement preparedStatement = db.getPrepStatement(query);
                preparedStatement.setString(1, namaPelanggan);
                preparedStatement.setString(2, alamat);
                preparedStatement.setString(3, telpon);
                preparedStatement.setInt(4, customerId);
                if (preparedStatement.executeUpdate() <= 0) {
                    this.peringatan("Gagal mengubah customer!");
                } else {
                    this.peringatan("Berhasil mengubah customer!");
                    this.dispose();
                    ((ListCustomer) listCustomer).loadTabel();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

