    public FormCustomerEdit(ListCustomer listCustomer, int customerId) {
        initComponents();
        this.customerId = customerId;
        this.db = new DB(new String[]{"customer", "customer_id"});
        
        try {
            PreparedStatement prepareStatement = this.db.getPrepStatement("SELECT * FROM customer WHERE customer_id=? LIMIT 1;");
            prepareStatement.setInt(1, customerId);
            ResultSet result = prepareStatement.executeQuery();
            
            if (result.next()) {
                inputNamaPelanggan.setText(result.getString("customer_fullname"));
                inputNomorTelpon.setText(result.getString("customer_telepon"));
                inputAlamat.setText(result.getString("customer_alamat"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        this.listCustomer = listCustomer;
    }
    
